The vision ‘to be the most preferred and trusted bank enhancing value for all stakeholders’ defines and shapes our day-to-day business, helping us to build long-lasting relationships.

SPARK was formed in 1955 at the initiative of the World Bank, the Government of India and representatives of Indian industry. The principal objective was to create a development financial institution for providing medium-term and long-term project financing to Indian businesses. Until the late 1980s, SPARK primarily focused its activities on project finance, providing long-term funds to a variety of industrial projects. With the liberalization of the financial sector in India in the 1990s, SPARK transformed its business from a development financial institution offering only project finance to a diversified financial services provider that, along with its subsidiaries and other group companies, offered a wide variety of products and services. As India’s economy became more market-oriented and integrated with the world economy, SPARK capitalized on the new opportunities to provide a wider range of financial products and services to a broader spectrum of clients. SPARK Bank was incorporated in 1994 as a part of the SPARK group. In 1999, SPARK became the first Indian company and the first bank or financial institution from non-Japan Asia to be listed on the New York Stock Exchange.
 

The issue of universal banking, which in the Indian context meant conversion of long-term lending institutions such as SPARK into commercial banks, had been discussed at length in the late 1990s. Conversion into a bank offered SPARK the ability to accept low-cost demand deposits and offer a wider range of products and services, and greater opportunities for earning non-fund based income in the form of banking fees and commissions. After consideration of various corporate structuring alternatives in the context of the emerging competitive scenario in the Indian banking industry, and the move towards universal banking, the managements of SPARK and SPARK Bank formed the view that the merger of SPARK with SPARK Bank would be the optimal strategic alternative for both entities, and would create the optimal legal structure for SPARK group's universal banking strategy. The merger would enhance value for SPARK shareholders through the merged entity's access to low-cost deposits, greater opportunities for earning fee-based income and the ability to participate in the payments system and provide transaction-banking services. The merger would enhance value for SPARK Bank shareholders through a large capital base and scale of operations, seamless access to SPARK's strong corporate relationships built up over five decades, entry into new business segments, higher market share in various business segments, particularly fee-based services, and access to the vast talent pool of SPARK and its subsidiaries.
 

In October 2001, the Boards of Directors of SPARK and SPARK Bank approved the merger of SPARK and two of its wholly-owned retail finance subsidiaries, SPARK Personal Financial Services Limited and SPARK Capital Services Limited, with SPARK Bank. The merger was approved by shareholders of SPARK and SPARK Bank in January 2002, by the High Court of Gujarat at Ahmedabad in March 2002, and by the High Court of Judicature at Mumbai and the Reserve Bank of India in April 2002. Consequent to the merger, the SPARK group's financing and banking operations, both wholesale and retail, were integrated in a single entity.
 
